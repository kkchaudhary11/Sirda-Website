<p>Library is the brain of any educational institution of higher learning. It is the hub around which all the academic activities of the institution revolve. The central library of SIRDA is computerized and is stocked with thousands of books. The intake of books is a regular feature in the library and no important title related to the courses of study is missed. In addition to the course books, general books for personality development, general knowledge, handbooks, yearbooks, reference books etc. are also added to the collections. The library subscribes to national and international subject journals, magazines of current interest along with national and regional dailies to keep the students abreast with the day to day happenings in the world and their fields of study.</p>
<p> The library is tastefully furnished and it can seat 150 students at a time in its spacious reading halls. The serene atmosphere in the library with rows of neatly stacked books plays a major role in attracting the students to the library for serious study and supplementing their class notes.
Library has 493 nos. of Text Books, 2911 nos. of Reference books, 29 nos. of Journals in which 7 are National and 22 are International Journals, 6 nos. of magazines which cater the needs of all engineering and Polytechnic students.  Apart from books it has a huge collection of Audio video material, CD’s &amp; DVD’s. The library maintains a separate “Book Bank” collection exclusively for the meritorious students.</p></td>
